# 参数和共享引用


def f(a):
    a = 99
b = 100
f(b)
print(b)

# >>>
# 100
# 使用f（b）调用函数时候，变量a赋值了对象100，但是
# a只是存在于调用函数之中，在函数中修改a对于调用函数的地方无影响
# 它直接把本地变量a重置为一个完全不同的对象


"""
传入可变对象
"""

def f2(a,b):
    a = 2
    b[0] = 'ajsjjs'

x = 100
y = ['a','v','c']
f2(x,y)
print(x)
print("")
print(y)
# >>>
# 100
#
# ['ajsjjs', 'v', 'c']






















































